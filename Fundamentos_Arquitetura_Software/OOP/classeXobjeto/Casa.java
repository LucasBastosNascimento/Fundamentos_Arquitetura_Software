package classeXobjeto;

public class Casa {
	
	public int TamanhoM2;
	public int Andares;
	public float Valor;
	public int NumerosVagas;
	
	public int getTamanhoM2() {
		return TamanhoM2;
	}
	public void setTamanhoM2(int tamanhoM2) {
		TamanhoM2 = tamanhoM2;
	}
	public int getAndares() {
		return Andares;
	}
	public void setAndares(int andares) {
		Andares = andares;
	}
	public float getValor() {
		return Valor;
	}
	public void setValor(float valor) {
		Valor = valor;
	}
	public int getNumerosVagas() {
		return NumerosVagas;
	}
	public void setNumerosVagas(int numerosVagas) {
		NumerosVagas = numerosVagas;
	}
	
	
	 

}
